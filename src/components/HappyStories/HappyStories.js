class HappyStories extends Component {
    constructor() {
        super();
        this.state = {
            active: "FIRST"
        };
    }
    handleClick() {
        var active = this.state.active;

        var newActive = active === "FIRST" ? "SECOND" : (active === 'SECOND' ? "THIRD": "FIRST");
        this.setState({
            active: newActive
        });
    }
    render() {
        var active = this.state.active;
        return (
            <div id='bg'>
            <h1 id='happyh1' className='text-center'>Happy Stories</h1>
                <div className="container">
                    <h1 className='text-center'></h1>
                    {active === "FIRST" ? <First /> : active === "SECOND" ? <Second /> : active === "THIRD" ? <Third /> : <First />}
                    <div className="container text-center">
                        <button
                            id='happybtn'
                            className="btn btn-lg"
                            onClick={this.handleClick.bind(this)}
                        >
                            MORE STORIES
                        </button>
                    </div>
                </div>
            </div>
        );
    }
}
export default HappyStories;